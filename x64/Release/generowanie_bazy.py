import random


def PESEL(imie):
    pesel_ = []
    rok = random.randint(1900, 2018)
    if rok < 2000:
        miesiac = random.randint(1, 12)
    elif rok < 2100:
        miesiac = random.randint(1, 12) + 20
    elif rok < 2200:
        miesiac = random.randint(1, 12) + 40
    elif rok < 2300:
        miesiac = random.randint(1, 12) + 60

    dzien = random.randint(1, 30)
    seria = []
    for i in range(0, 3):
        seria.append(str(random.randint(0, 9)))
    if imie.endswith("a"):
        seria.append(str(random.choice([0, 2, 4, 6, 8])))
    else:
        seria.append(str(random.choice([1, 3, 5, 7, 9])))
    rok = list(str(rok))
    if dzien < 10:
        dzien = "0" + str(dzien)
    if miesiac < 10:
        miesiac = "0" + str(miesiac)

    pesel_ = rok[2:] + list(str(miesiac)) + list(str(dzien)) + seria
    kontrolna = 9 * int(pesel_[0]) + 7 * int(pesel_[1]) + 3 * int(pesel_[2]) + 1 * int(pesel_[3]) + 9 * int(
        pesel_[4]) + 7 * int(pesel_[5]) + 3 * int(pesel_[6]) + 1 * int(pesel_[7]) + 9 * int(pesel_[8]) + 7 * int(
        pesel_[9])
    pesel = ""

    for x in pesel_:
        pesel += x
    kontrolna = kontrolna % 10
    pesel += str(kontrolna)
    return pesel


